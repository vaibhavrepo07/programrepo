#include<bits/stdc++.h>
using namespace std;

int main()
{
	cout<<"Logical AND Operator"<<endl;
	cout<<((10<20)&&(30<40));
	cout<<((10<30)&&(30>40));
	
	cout<<"Logical OR Operator"<<endl;
	cout<<((10<=30)||(30>89));
	cout<<((89>34)||(28>=87));
	
	cout<<"Logical NOT Operator"<<endl;
	cout<<(!(20<49));
	cout<<(!(34<=78));
	
	return 0;
	
}
