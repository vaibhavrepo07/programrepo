//Program for Switch Statements
#include<bits/stdc++.h>
using namespace std;
int main()
{
	int a ;
	cout<<"Let's do Coding wiht Switch statements"<<endl;
	cin>> a;
	switch(a)
	{
		case 1: cout<<"Hello "<<endl;
		break;
		case 2: cout<<"How ?"<<endl;
		break;
		case 3: cout<<"Are You"<<endl;
		break;
		
		
	default :
		cout<<"out"<<endl;
	}
}
